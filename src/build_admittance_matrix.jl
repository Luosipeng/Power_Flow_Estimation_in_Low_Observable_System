using LinearAlgebra
using SparseArrays

"""
构建三相导纳矩阵 (针对平衡系统扩展)
输入: branch_data (矩阵), 每一行格式: [f_bus, t_bus, r, x, b, ..., status, ...]
输出: Y_bus (稀疏复数矩阵, 99x99)
"""
function build_three_phase_admittance_matrix(branch_data::AbstractMatrix)
    # 1. 确定系统规模
    # 第1列是 From Bus, 第2列是 To Bus，找出最大节点号
    max_bus = Int(maximum([branch_data[:, 1]; branch_data[:, 2]]))
    
    num_phases = 3
    dim = max_bus * num_phases
    
    # 初始化稀疏矩阵
    Y_bus = spzeros(ComplexF64, dim, dim)

    # 2. 遍历每一行支路数据
    num_branches = size(branch_data, 1)
    
    for i in 1:num_branches
        # 获取状态 (第11列)
        status = branch_data[i, 11]
        
        # 如果开关断开 (0.0)，跳过该支路
        if status == 0.0
            continue
        end

        # 解析参数
        f_bus = Int(branch_data[i, 1])
        t_bus = Int(branch_data[i, 2])
        r = branch_data[i, 3]
        x = branch_data[i, 4]
        b = branch_data[i, 5]  # Total Susceptance (B)

        # 计算单相导纳
        z_s = r + im * x
        if abs(z_s) == 0
            println("Warning: Zero impedance branch at row $i (Nodes $f_bus-$t_bus)")
            continue
        end
        
        y_s = 1.0 / z_s            # 串联导纳 (Series Admittance)
        y_sh = im * b / 2.0        # 半边并联导纳 (Shunt Admittance, Pi模型)

        # 3. 构建 3x3 子矩阵 (平衡系统假设：对角阵)
        # 三相参数相同，且忽略相间互感
        # 串联块: diag(y_s, y_s, y_s)
        Y_series_blk = Diagonal(fill(y_s, 3))
        # 并联块: diag(y_sh, y_sh, y_sh)
        Y_shunt_blk  = Diagonal(fill(y_sh, 3))
        
        # 自导纳贡献 (Self) = 串联 + 并联
        Y_self = Y_series_blk + Y_shunt_blk

        # 4. 填充大矩阵 Ybus
        # 计算在 99x99 矩阵中的索引范围
        # Node k -> indices [(k-1)*3+1 : k*3]
        idx_f = (f_bus - 1) * num_phases + 1 : f_bus * num_phases
        idx_t = (t_bus - 1) * num_phases + 1 : t_bus * num_phases

        # 更新对角块 (注入电流节点)
        Y_bus[idx_f, idx_f] += Y_self
        Y_bus[idx_t, idx_t] += Y_self

        # 更新非对角块 (节点间互导纳) - 注意符号为负
        Y_bus[idx_f, idx_t] -= Y_series_blk
        Y_bus[idx_t, idx_f] -= Y_series_blk
    end

    return Y_bus
end

